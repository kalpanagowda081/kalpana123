package com.kalpana.java;

import java.util.Scanner;

public class additionOperater {
    public static void main (String[] args){
        Scanner add = new Scanner(System.in);
        System.out.println(" please enter the first values");
        int a = add.nextInt();
        System.out.println(" please enter the second values");
        int b = add.nextInt();
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a%b);
    }






}
